public class Array_Object{
    public static void main(String[] args)
    {
        int nums [] = new int[6];
        nums[0] = 1;
        nums[1] = 2;
        nums[2] = 3;
        nums[3] = 4;
        nums[4] = 5;
        nums[5] = 6;

        for(int i = 0; i < nums.length; i++){
            System.out.println(nums[i]);
        }
    }
}