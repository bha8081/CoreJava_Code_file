public class Play3
{
    private String name;
    private int age;
    private String game;

    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
    public int getAge()
    {
        return age;
    }
    public void setAge(int age)
    {
        this.age = age;
    }
    public String getGame()
    {
        return game;
    }
    public void setGame(String game)
    {
        this.game = game;
    }
}